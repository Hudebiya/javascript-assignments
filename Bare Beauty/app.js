var productMenu = document.getElementById("productMenu");
var brandMenu = document.getElementById("brandMenu");
var allProducts = document.getElementById("allProducts");

function createCard(p) {
    return `
        <div class="col">
            <div class="card">
                <img src="${p.src}" class="card-img-top" alt="${p.Name}">
                <div class="card-body">
                    <h5 class="card-title">${p.Name}</h5>
                    <p class="card-text">${p.Description}</p>
                </div>
                <div class="card-footer d-flex justify-content-between align-items-center">
                    <h5 class="price">Rs.${p.price}/-</h5>
                    <button class="btn btn-add-to-cart btn-sm">Add to Cart</button>
                </div>
            </div>
        </div>
    `;
}

for (var category in beautyProducts) {
    productMenu.innerHTML += `<option value="${category}">${category.toUpperCase()}</option>`;
}

function viewAll() {
    allProducts.innerHTML = "";
    let totalProducts = 0;

    for (var category in beautyProducts) {
        for (var brand in beautyProducts[category]) {
            for (var item in beautyProducts[category][brand]) {
                allProducts.innerHTML += createCard(beautyProducts[category][brand][item]);
                totalProducts++;
            }
        }
    }

    productMenu.value = "";
    brandMenu.innerHTML = `<option value="" selected disabled>Select Brand</option>`;

    if (totalProducts === 0) {
        allProducts.innerHTML = '<div class="col-12"><div class="alert alert-info">No products found.</div></div>';
    }
}

function productChange() {
    brandMenu.innerHTML = `<option value="" selected disabled>Select Brand</option>`;
    allProducts.innerHTML = "";

    var selectedCategory = productMenu.value;

    if (selectedCategory && beautyProducts[selectedCategory]) {
        for (var brand in beautyProducts[selectedCategory]) {
            brandMenu.innerHTML += `<option value="${brand}">${brand.toUpperCase()}</option>`;
        }
        filterProduct(selectedCategory);
    } else {
        viewAll();
    }
}

function filterProduct(categoryOverride) {
    allProducts.innerHTML = "";
    var category = categoryOverride || productMenu.value;
    var brand = brandMenu.value;

    let foundProducts = 0;

    if (category && brand && brand !== "") {
        if (beautyProducts[category] && beautyProducts[category][brand]) {
            for (var item in beautyProducts[category][brand]) {
                allProducts.innerHTML += createCard(beautyProducts[category][brand][item]);
                foundProducts++;
            }
        }
    } else if (category) {
        for (let b in beautyProducts[category]) {
            for (let item in beautyProducts[category][b]) {
                allProducts.innerHTML += createCard(beautyProducts[category][b][item]);
                foundProducts++;
            }
        }
    }

    if (foundProducts === 0) {
        allProducts.innerHTML = '<div class="col-12"><div class="alert alert-info">No products found for your selection.</div></div>';
    }

    allProducts.innerHTML += `
        <div class="col-12 text-center mt-5">
            <button onclick="viewAll()" class="btn btn-lg btn-view-all">
                View All Products
            </button>
        </div>
    `;
}

window.onload = viewAll;