var productMenu = document.getElementById("productMenu");
var brandMenu = document.getElementById("brandMenu");
var allProducts = document.getElementById("allProducts");

for (var category in beautyProducts) {
  productMenu.innerHTML += `
    <option value="${category}">${category.toUpperCase()}</option>
  `;
}

function productChange() {
  brandMenu.innerHTML = `<option selected>Select Brand</option>`;

  var selectedCategory = productMenu.value;

  for (var brand in beautyProducts[selectedCategory]) {
    brandMenu.innerHTML += `
      <option value="${brand}">${brand.toUpperCase()}</option>
    `;
  }
}

function viewAll() {
  allProducts.innerHTML = "";

  for (var category in beautyProducts) {
    for (var brand in beautyProducts[category]) {
      for (var item in beautyProducts[category][brand]) {
        var p = beautyProducts[category][brand][item];

        allProducts.innerHTML += createCard(p);
      }
    }
  }
}


function filterProduct() {
  allProducts.innerHTML = "";

  var category = productMenu.value;
  var brand = brandMenu.value;

  for (var item in beautyProducts[category][brand]) {
    var p = beautyProducts[category][brand][item];
    allProducts.innerHTML += createCard(p);
  }

  allProducts.innerHTML += `
    <div class="col btnCol">
      <button onclick="viewAll()" class="btn allBtn btn-outline-success">
        View All Products
      </button>
    </div>
  `;
}

function createCard(p) {
  return `
    <div class="col">
      <div class="card">
        <img src="${p.src}" class="card-img-top" alt="">
        <div class="card-body">
          <h5 class="card-title">${p.Name}</h5>
          <p class="card-text">${p.Description}</p>
        </div>
        <div class="card-footer">
          <h5 class="price">Rs.${p.price}/-</h5>
        </div>
      </div>
    </div>
  `;
}

// Load all on first page
viewAll();
